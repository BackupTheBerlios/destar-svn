<h2 id="contentHeading">Welcome</h2>

<p>Destar is a web-based interface to manage the Asterisk PBX.</p>
<p>It provides high-level abstraction of the Asterisk configuration.</p>

<p><i>Download the latest version: <b><a href="/index.php?c=2">destar-0.2.1</a></b></i></p>

<p>Versions 0.2.X contain many improvements over the 0.1 one:<br /> 
<ul style="font-family: verdana;">   
<li>A new interface style (see <a href="manual/images/">screenshots</a>).   </li>   
<li>Referencial integrity.   </li>   
<li>Better i18n support (english, spanish and french right now).   </li>   
<li>Better statistics for CDR.</li>   
<li>Updated for <a href="http://www.asterisk.org">asterisk</a> 1.2.X.<br />  </li>   
<li>Many new features.   </li>   
<li>Several Bug fixes.</li>
<li>First version of <a href="manual/">user manual</a></li> 
</ul> 

<p>Destar main features include:</p>
<ul>
<li>Hosted PBX - virtual PBX features.</li>
<li>Extensions management: SIP, IAX, Zap, and more.</li>
<li>Auto-attendants support.</li>
<li>Trunks management: SIP, IAX, Zap, ZapPRI, and more.</li>
<li>Use of dialout patterns (i.e. local, national, mobile-phones).</li>
<li><a href="http://www.asternic.org">Asternic Flash Operator Panel</a> integration.</li>
<li>Call Detail Records search and graphical reports.</li>
<li>Many application applets incluided: Voice Mail, Meeting Room, and more.</li>
<li>Extensible pluguin-based architecture.</li>
</ul>

